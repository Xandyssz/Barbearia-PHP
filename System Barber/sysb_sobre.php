	<h1>Sobre nós</h1>
	<h3>
		O “System Barber” é um sistema Web que tem a finalidade principal de facilitar o processo de agendamento de serviços relacionados a uma única barbearia, permitindo que essas pessoas efetuem agendamentos sem sair de casa e otimizando seu tempo.
Os usuários que querem realizar algum agendamento, precisam se cadastrar no sistema, mas a navegação não depende de um cadastro. 
O administrador, poderá adicionar serviços e seus respectivos valores, cadastrar profissionais, e removê-los também , adicionar e cancelar agendamentos, também terá acesso a lista de todos usuários cadastrados, terá a permissão de adicionar e  remover usuário.
O usuário, poderá agendar o serviço de seu interesse, terá acesso a tabela de preços, a data e o horário, o profissional que irá atendê-lo,  visualizar as avaliações e avaliar também.
	O cliente poderá agendar um serviço com um profissional, ou agendar mais de um serviço com um  barbeiro de sua preferência, caso queira, poderá também marcar mais de um serviço com profissionais diferentes de sua escolha.
	Caso o cliente agende um serviço e não compareça esse será multado em seu próximo atendimento, para que não ocorra nenhum conflito, o administrador confirmará se o mesmo compareceu ou não. Se o cliente agendou um serviço e não puder comparecer será possível cancelar sua visita.
	Para que o usuário não tenha dúvidas ou caso não se lembre de seu serviço agendado, será possível que ele verifique seus agendamentos, caso tenha.

	</h3>
